void main() {
  //normal single quotes
  var singleline = 'am in single quotes';
  print(singleline);

  //double quotes
  var doublequotes = "am in double quotes";
  print(doublequotes);

  //with an apostrophe
  dynamic demo = 'it\'s five in the morning';
  print(demo);

  demo = "it's easier this way";
  print(demo);

  //multiline strings
  var sentense = ''' it was on a sunday morning,
when I decided to start learning dart programming.''';

  print(sentense);
}
